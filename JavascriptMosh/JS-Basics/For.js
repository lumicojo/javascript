//  For
for (let i = 0; i < 5; i++) {
  console.log('Hello World!');
  // to se in console put i on end console.log('Hello World!', i);
}
/// or
for (let i = 1; i <= 5; i++) {
  console.log('Hello World!');
}

// add number
for (let i = 1; i <= 5; i++) {
  if (i % 2 !== 0) console.log(i); // = 1,3,5
}
/////  Reverse order
for (let i = 5; i >= 1; i--) {
  if (i % 2 !== 0) console.log(i);  // = 5,3,1
}
